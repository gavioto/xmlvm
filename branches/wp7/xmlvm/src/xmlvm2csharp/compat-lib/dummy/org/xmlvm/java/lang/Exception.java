package org.xmlvm.java.lang;

public class Exception extends org.xmlvm.java.lang.Throwable {

    public Exception() {
	super();
    }

    public Exception(java.lang.String message) {
	super(message);
    }

    public Exception(java.lang.String message, java.lang.Throwable cause) {
	super(message, cause);
    }

    public Exception(java.lang.Throwable cause) {
	super(cause);
    }
}